namespace DotNet.Highcharts.Options
{
    public class ContainerOptions
    {
        /// <summary>
        /// By default, highcharts will set a default height if the parent object doesn't have a height declared.
        /// By setting this option to 'true', the container object will set it's height to '100%' to match its
        /// parent.
        /// </summary>
        public bool MatchParentHeight { get; set; }
    }
}
